    """Top-level cover generator. Composes all per-heuristic strategies."""
    profile: dict
    pool: PoolState

    @classmethod
    def from_profile(cls, pool: Optional[PoolState] = None) -> "CoverGenerator":
        return cls(profile=_load_profile(), pool=pool or PoolState())

    def generate(self, intent: UserIntent) -> CoverParams:
        adjusted = dict(intent.raw_params)
        rationale = {}

