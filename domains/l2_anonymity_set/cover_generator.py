        result_h2 = _strategy_h2(self.profile, intent, self.pool)
        rationale["H2"] = result_h2.pop("_reason", "")
        adjusted.update({k: v for k, v in result_h2.items() if not k.startswith("_")})
        result_h3 = _strategy_h3(self.profile, intent, self.pool)
        rationale["H3"] = result_h3.pop("_reason", "")
        adjusted.update({k: v for k, v in result_h3.items() if not k.startswith("_")})
        result_h4 = _strategy_h4(self.profile, intent, self.pool)
        rationale["H4"] = result_h4.pop("_reason", "")
        adjusted.update({k: v for k, v in result_h4.items() if not k.startswith("_")})
        result_h5 = _strategy_h5(self.profile, intent, self.pool)
        rationale["H5"] = result_h5.pop("_reason", "")
